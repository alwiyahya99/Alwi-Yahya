package pbo2.pkg10117050.latihan18.kelipatan;

public class PBO210117050LATIHAN18KELIPATAN {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {

        double nKelipatan = 3.5;

        while (nKelipatan <= 35) {

            System.out.println(nKelipatan);
            nKelipatan = nKelipatan + 3.5;
        }
    }

}
