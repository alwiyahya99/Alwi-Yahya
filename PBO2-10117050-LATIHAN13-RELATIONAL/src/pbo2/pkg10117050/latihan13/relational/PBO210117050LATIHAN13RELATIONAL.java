package pbo2.pkg10117050.latihan13.relational;

public class PBO210117050LATIHAN13RELATIONAL {

    public static void main(String[] args) {
        int bill1 = 10;
        int bill2 = 20;
        System.out.println("a == b = " + (bill1 == bill2));
        System.out.println("a != b = " + (bill1 != bill2));
        System.out.println("a > b = " + (bill1 > bill2));
        System.out.println("a < b = " + (bill1 < bill2));
        System.out.println("a >= b = " + (bill2 >= bill1));
        System.out.println("a <= b = " + (bill2 <= bill1));
    }
    
}
